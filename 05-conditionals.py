#!/usr/bin/env python

# Boolean expressions
print('1)', 3 == 3)
print('2)', 3 == 2)
print('3)', type(True))

# Logical operators
print('4)', True and True)
print('5)', True and False)
print('6)', True or not True)

# If statement
some_condition = False
if some_condition:
    print('The variable \'some_condition\' is True')
else:
    print('The variable \'some_condition\' is False')
